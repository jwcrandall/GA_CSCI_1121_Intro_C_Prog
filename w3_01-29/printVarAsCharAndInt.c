#include<stdio.h>
//Print two values as characters and integers
int main(){
  //Delcare and initialize variables
  char ch = 'a';
  int i = 56;

  //Print both variables as characters
  printf("Value of ch is %c; Value of i is %c \n", ch, i);
  //Print both variables as integers
  printf("Value of ch is %i; Value of i is %i \n", ch, i);
  //Exit program
  return 0;
}

